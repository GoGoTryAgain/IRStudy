void StartTimCapture(void)
{
	HAL_TIM_IC_Start_IT(&htim2,TIM_CHANNEL_1);
	HAL_TIM_IC_Start_IT(&htim2,TIM_CHANNEL_2);
}

void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
	static uint32_t Ltick = 0;
	static uint32_t Htick = 0;
	if(htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1)
	{

		__HAL_TIM_CLEAR_FLAG(&htim1,TIM_FLAG_UPDATE); //need to erase int first,otherwise it will into IQRhandel immediately
		HAL_TIM_Base_Start_IT(&htim1);
		if(IRperiodStartFlag != 0)
		{
			Htick = HAL_TIM_ReadCapturedValue(&htim2,TIM_CHANNEL_1);
			{
				AddIRData(1,(Htick - Ltick)*50);
				printf("Htick = %d\r\n",(Htick - Ltick)*50);
				Ltick = 0;
			}
		}
	}
	if(htim->Channel == HAL_TIM_ACTIVE_CHANNEL_2)
	{
		IRperiodStartFlag = 1;
		Ltick = HAL_TIM_ReadCapturedValue(&htim2,TIM_CHANNEL_2);
		AddIRData(0,Ltick*50);
		printf("Ltick = %d\r\n",Ltick * 50);
		Htick = 0;
	}
	
}




